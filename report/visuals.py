def inject_visuals_into_markdown(md_content: str, results: dict) -> str:
    """Inject SVG charts and ASCII tables into Markdown report content."""
    profile = results.get("company_profile", {})
    seo = results.get("seo_keywords", {})
    competitor = results.get("competitor", {})
    swot = results.get("swot", {})
    score = results.get("prospect_score", {})

    lines = md_content.split("\n")
    new_lines: list[str] = []

    for line in lines:
        new_lines.append(line)

        # Inject SWOT quadrant after SWOT section header
        if line == "## 5. SWOT Analysis":
            model = build_swot_quadrant(swot)
            new_lines.append("")
            new_lines.append("```")
            new_lines.append(swot_quadrant_ascii(model))
            new_lines.append("```")
            new_lines.append("")

        # Inject competitor radar chart
        if line == "## 3. Competitor Landscape":
            model = build_radar_chart(competitor)
            if model.series:
                new_lines.append("")
                new_lines.append("```")
                new_lines.append(radar_chart_ascii(model))
                new_lines.append("```")
                new_lines.append("")

        # Inject content gap bar chart
        if line == "## 2. SEO & Keyword Analysis":
            model = build_content_gap_chart(seo)
            if model.items:
                new_lines.append("")
                new_lines.append("```")
                new_lines.append(bar_chart_ascii(model))
                new_lines.append("```")
                new_lines.append("")

        # Inject score donut
        if line == "## 9. Prospect Score":
            model = build_score_donut(score)
            new_lines.append("")
            new_lines.append("```")
            new_lines.append(score_donut_ascii(model))
            new_lines.append("```")
            new_lines.append("")

    # Add roadmap at end before evidence
    roadmap_items = build_automation_roadmap(swot)
    roadmap_text = roadmap_ascii(roadmap_items)
    evidence_marker = "## Evidence Sources"
    final_lines: list[str] = []
    for line in new_lines:
        if line == evidence_marker:
            final_lines.append("")
            final_lines.append("## Automation Roadmap")
            final_lines.append("")
            final_lines.append(roadmap_text)
            final_lines.append("")
            final_lines.append("---")
            final_lines.append("")
        final_lines.append(line)

    return "\n".join(final_lines)


def inject_visuals_into_html(html_content: str, results: dict) -> str:
    """Inject SVG charts into HTML report content (replaces ASCII placeholders)."""
    profile = results.get("company_profile", {})
    seo = results.get("seo_keywords", {})
    competitor = results.get("competitor", {})
    swot = results.get("swot", {})
    score = results.get("prospect_score", {})

    # SWOT SVG
    swot_model = build_swot_quadrant(swot)
    swot_svg = swot_quadrant_svg(swot_model)
    html_content = html_content.replace(
        "<code>┌──────────────────┬──────────────────┐",
        swot_svg
    )

    # Radar chart SVG
    radar_model = build_radar_chart(competitor)
    if radar_model.series:
        radar_svg = radar_chart_svg(radar_model)
        html_content = html_content.replace(
            "<code>Competitor",
            radar_svg
        )

    # Bar chart SVG
    bar_model = build_content_gap_chart(seo)
    if bar_model.items:
        bar_svg = bar_chart_svg(bar_model)
        html_content = html_content.replace(
            "<code>**Content Gaps",
            bar_svg
        )

    # Score donut SVG
    score_model = build_score_donut(score)
    if score_model.overall > 0:
        score_svg = score_donut_svg(score_model)
        html_content = html_content.replace(
            "<code>**Score:",
            score_svg
        )

    # Roadmap SVG
    roadmap_items = build_automation_roadmap(swot)
    roadmap_s = roadmap_svg(roadmap_items)
    html_content = html_content.replace(
        "<h2>Automation Roadmap</h2>",
        f"<h2>Automation Roadmap</h2>{roadmap_s}"
    )

    return html_content
