"""Visual report builder — data models, SVG generation, and ASCII fallbacks.

Pure SVG charts that work in Markdown (via <img> data URIs), HTML reports,
and PDF export (WeasyPrint renders inline SVG natively).

No external charting dependencies — all SVG is generated by hand.
"""
